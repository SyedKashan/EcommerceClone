$("#pwd").keyup(function(){
		var pwd = $("#pwd").val();
		if (pwd == ''){
		$("#chkpwd").html("");}
		else{
		$.ajax({
			type:'get',
			url:'/admin/check-pwd',
			data:{pwd:pwd},
			success: function(resp){

				if (resp == 'false'){
					$("#chkpwd").html("<font color='brown'>Current Password is wrong!</font>");	
				}
				else{
					$("#chkpwd").html("<font color='green'>Current Password is right!</font>");	
				}
			},
			error:function(resp){
				
				alert("Error");
			}
		});}
	});
	
